package Encapsulation;

public class Student {
	private String name;
	private int rollno;
	
	public void setName(String s)
	{
		name = s;
	}
	public String getName()
	{
		return name;
	}
	public void setRollno(int r)
	{
		rollno = r;
	}
	public int getRollno()
	{
		return rollno;
	}

}
